public class Calculator {
    public int sum(int x, int y) {
        int summa = x + y;
        return summa;
    }

    public int minus(int x, int y){
        int minus = x - y;
        return minus;
    }
    public int multiplication(int x, int y){
        int mult = x * y;
        return mult;
    }
    public double div(int x, int y){
        double div = x / (double) y;
        return div;
    }
}